void setRasterFont(void* font)
{
	BitmapFontPtr	fontp=(BitmapFontPtr)font;//&glutBitmapTimesRoman10;
	curFont = fontp;
	GLuint i, j;
	glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
	const BitmapCharRec *ch;
	for(i=0;i<32;i++)
	{
		glNewList(fontOffset + i, GL_COMPILE);
		glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, space);
		glEndList();

	}
	for(i=32;i<256;i++)
	{
		ch=fontp->ch[i-fontp->first];
		if(ch){
			glNewList(fontOffset + i, GL_COMPILE);
			glBitmap(ch->width, ch->height, ch->xorig, ch->yorig,
					ch->advance, 0, ch->bitmap);
			glEndList();

		}
		else{
			glNewList(fontOffset + i, GL_COMPILE);
			glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, space);
			glEndList();

		}
	}
}

